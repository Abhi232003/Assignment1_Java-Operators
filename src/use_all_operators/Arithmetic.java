package use_all_operators;

public class Arithmetic {

	public static void main(String[] args) {
		int a=5,b=10;
		
		System.out.println("Addition of 5 and 10 is "+(a+b));
		System.out.println("Subtraction of 5 and 10 is "+(a-b));
		System.out.println("Multiplication of 5 and 10 is "+(a*b));
		System.out.println("Division of 5 and 10 is "+(a/b));


	}

}
